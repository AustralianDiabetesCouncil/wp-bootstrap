          <p class="lead">Diabetes is a lifelong condition. There are times when people with diabetes need some extra advice, have questions or have problems they can’t manage on their own. It is reassuring to know that there are many specially trained professionals to assist people with diabetes to better health.</p>


          <strong>The team:</strong></p>
          <ul>
            <li class="normaltext">
              <strong>You</strong>, the person with diabetes. You are the most important part of your team. Your family, friends and coworkers might be part of your team as well. It depends on you and your needs.</li>
            <li class="normaltext">
              <strong>Your family doctor</strong> often looks after your diabetes and referrals to other health professionals as needed. Your family doctor is often responsible for ordering your ongoing blood and other diabetes tests.</li>
            <li class="normaltext">
              <strong>An Endocrinologist or Diabetes specialist</strong> is a doctor who has done years of special training in diabetes. Many people with type 1 diabetes see an endocrinologist. People with type 2 diabetes may see an endocrinologist if they are having trouble with their diabetes management or if they are having other health problems impacting on their diabetes.</li>
            <li class="normaltext">
              <strong>A Diabetes Educator</strong> is usually a Registered Nurse who has done special training in diabetes. Educators can assist with teaching you about diabetes, blood glucose monitoring, medications, insulin, sick days, travel, stress and just about anything else. <a href="/PDFs-(other)/Diabetes-Services_Locality-Guide_July2010.aspx">Find a Diabetes Service in your area</a> (NSW only).</li>
            <li class="normaltext">
              <strong>A Dietitian</strong> can answer questions about healthy eating for you and your family. To find a dietitian in your area first call your <a href="/PDFs-(other)/Diabetes-Services_Locality-Guide_July2010.aspx">local diabetes service</a> or contact the <a href="http://www.daa.asn.au/" target="_blank">Dietitians Association of Australia</a> on 1800 812 942.</li>
            <li class="normaltext">
              <strong>An Exercise Physiologist</strong> can help to develop a physical activity plan suitable for anyone - regardless of age, ability or disability.</li>
            <li class="normaltext">
              <strong>An Optometrist</strong> can do a diabetes eye check and a vision check. Some people with diabetes need to see an Ophthalmologist, a doctor with special training in diseases and problems with the eye.</li>
            <li class="normaltext">
              <strong>Podiatrists</strong> are health professionals who deal with the feet. Many podiatrists have advanced training in the ‘diabetic foot’. To help you find a podiatrist who has specialist training contact the <a href="http://www.podiatry.asn.au/" target="_blank">Australian Podiatry Association</a> (NSW) on (02) 9698 3751.</li>
          </ul>
          <p>
            Sometimes people with diabetes have trouble coping with the day to day burden of their disease. Social workers, psychologists can help in this area. Your family doctor or diabetes educator can often refer you to these services.</p>
          <p>
            Other specialists are sometimes needed. Children with diabetes may see a paediatrician or a paediatric endocrinologist. Women with diabetes who are planning a pregnancy, who are pregnant or women who develop gestational diabetes usually see an obstetrician. If complications of diabetes are present, referral to other kinds of health professionals may be required.</p>
